package Basic;

public class Lab013 {
    public static void main(String[] args) {
        // Ternary Operator
        // ? - if this do 1 statement, then 2 statement

        boolean result = 10>40 ? 10>40 : 40>10;
        System.out.println(result);

        String str = 10>20 ? "10" : "twenty";
        System.out.println(str);


    }
}
